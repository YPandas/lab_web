<script>
import ProfileCard from '@/components/ProfileCard.vue';
import DingImage from '@/assets/profile/Ding.png';
import gsap from 'gsap';
import ScrollTrigger from 'gsap/ScrollTrigger';

gsap.registerPlugin(ScrollTrigger);

export default {
  name: 'TeamPage',
  components: {
    ProfileCard
  },
  data() {
    return {
      DingImage: DingImage
    }
  },
  mounted() {
    // Animate staff titles
    document.querySelectorAll('.staff-title').forEach(title => {
      gsap.from(title, {
        scrollTrigger: {
          trigger: title,
          start: "top 80%",
          toggleActions: "play none none none" // Only play once when entering view
        },
        y: 50,
        opacity: 0,
        duration: 1,
        ease: 'power2.out'
      });
    });

    // Animate profile cards with stagger for each section
    document.querySelectorAll('.staff-list').forEach(list => {
      gsap.from(list.querySelectorAll('.profile-card'), {
        scrollTrigger: {
          trigger: list,
          start: "top 80%",
          toggleActions: "play none none none" // Only play once when entering view
        },
        y: 100,
        opacity: 0,
        duration: 1,
        stagger: {
          amount: 0.8,
          from: "start"
        },
        ease: 'power2.out'
      });
    });
  }
}
</script>

